function getBasicColorName(r,g,b)
    best_name = ""
    best_dist = Inf

    for (name, col) in basic_colors

        dist = (r - col[1])^2 +
               (g - col[2])^2 +
               (b - col[3])^2

        if dist < best_dist
            best_dist = dist
            best_name = name
        end
    end
    return best_name
    
end

function getJuliaColorName(r,g,b)
    best_name = ""
    best_dist = Inf

    for (name, col) in JuliaColorsNames

        dist = (r - col[1])^2 +
               (g - col[2])^2 +
               (b - col[3])^2

        if dist < best_dist
            best_dist = dist
            best_name = name
        end
    end
    return best_name
end

